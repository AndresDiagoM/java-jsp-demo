public class condicionales{
    public static void main(String[] args) {
        
        // condicionales
        int l = 5;
        int m = 10;
        if (l > m) {
            System.out.println("l es mayor que m");
        } else if (l < m) {
            System.out.println("l es menor que m");
        } else {
            System.out.println("l es igual a m");
        }
    }
}